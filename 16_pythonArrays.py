""" Python Arrays """

# 01
# Create an array
cars = ['Ford', 'Volvo', 'BMW']
print(cars)

# 02
# Access the elements of an array
cars = ['Ford', 'Volvo', 'BMW']
x = cars[0]
print(x)

# 03
# Change the value of an array element
cars = ['Ford', 'Volvo', 'BMW']
cars[0] = 'Toyota'
print(cars)

# 04
# Get length of an array
cars = ['Ford', 'Volvo', 'BMW']
print(len(cars))

# 05
# Loop through all elements of an array
cars = ['Ford', 'Volvo', 'BMW']
for x in cars:
    print(x)

# 06
# Add an element to an array
cars = ['Ford', 'Volvo', 'BMW']
cars.append('Honda')
print(cars)

# 07
# Remove an element from an array
cars = ['Ford', 'Volvo', 'BMW']
cars.pop(1)
print(cars)