""" Python Casting """

# 01
# Casting integers
x = int(1)
y = int(2.8)
z = int("3")
print(type(x))
print(type(y))
print(type(z))

# 02
# Casting floats
x = float(1)
y = float(2.8)
z = float("3")
w = float("4.2")
print(type(x))
print(type(y))
print(type(z))
print(type(w))

# 03
# Casting String
x = str("s1")
y = str(2)
z = str(3.0)
print(type(x))
print(type(y))
print(type(z))