""" Python Numbers """

# 01
# Verify the type of an object
x = 1
y = 2.8
z = 1j
print(type(x))
print(type(y))
print(type(z))

# 02 
# Create integers
x = 1
y = 3565222554887711
z = -356522
print(type(x))
print(type(y))
print(type(z))

# 03
# Create floating point numbers
x = 1.10
y = 1.0
z = -35.59
print(type(x))
print(type(y))
print(type(z))

# 04
# Create scientific numbers with an "e" to indicate the power of 10
x = 35e3
y = 12E4
z = -87.7e100
print(type(x))
print(type(y))
print(type(z))

# 05
# Create complex numbers
x = 3+5j
y = 5j
z = -5j
print(type(x))
print(type(y))
print(type(z))